#include "libft.h"

void	ft_sort_param(int ac, char **av)
{
	int	j;
	int	x;

	x = 1;
	while (x < (ac - 1))
	{
		j = ft_strcmp(av[x], av[x + 1]);
		if (j > 0)
		{
			ft_swap(&av[x], &av[x + 1]);
			x = 1;
		}
		else
			x++;
	}
}
